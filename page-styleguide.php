<h5>Dwarf Planets</h5>
<p>Pluto and its moon Charon are placed near the southern of the Dellen lakes. Ixion, a dwarf planet candidate, is located at Technichus, a science center in H&#228;rn&#246;sand, 360 km from the Globe. Eris is located at Umestans F&#246;retagspark, 510 km from the Globe. Sedna, another dwarf planet candidate, is located at Teknikens Hus, 912 km from the Globe.</p>

<h6>Other Bodies</h6>
<p>The near-Earth Object Eros is located at M&#246;rbyskolan, a school in Danderyd Municipality. The asteroid Saltis is located at Saltsj&#246;baden's Kunskapsskola, a school near the Stockholm Observatory.  The asteroid Palomar-Leiden is located in a park in Alsike. Halley's Comet is located at Balthazar Science Center. Comet Swift-Tuttle is placed at Kreativum, a science center in Karlshamn. The Terminal Shock is at the edge of the heliosphere, it is the boundary where the solar wind transitions to subsonic velocity. No sculpture currently represents the terminal shock, but a foundation for a future sculpture exists at the Institute of Space Physics.</p>



