<h1>The Sweden <br>Solar System</h1>

<p>The Sweden Solar System is the world's largest permanent scale model of the Solar System. The sun is represented by the <a href="http:www/go.com">Ericsson Globe</a> in Stockholm, the largest hemispherical building in the world. The inner planets can also be found in Stockholm but the outer planets are situated northward in other cities along the Baltic Sea. It was started by Nils Brenning and G&#246;sta Gahm. It is in the scale of 1:20 million.</p>

<h2>The System</h2>
<p>The bodies represented in this model include the Sun, the planets and some of their satellites, dwarf planets and many types of small bodies (comets, asteroids, trans-Neptunians, etc.), as well as some abstract concepts like the <a href="http:www/go.com">Termination Shock zone</a>. Because of the existence of many small bodies in the real Solar System, the model can always be further increased.</p>

<blockquote>The Globe Arena in Stockholm, which represents the Sun, is the largest spherical building in the world.</blockquote>

<p>A number of minor planets and comets also populate The Solar System, which now extends from the very south to the very north of Sweden.  There is a host institution for each model. The Sweden Solar System is a pedagogical instrument and conveys a direct feeling of the enormous distances in space, and how small the planets are compared to the Sun. Art, mythology and science merge in this project, and The Solar System connects many different places and different type of activities around Sweden. </p>


<h3>Inner Planets</h3>
<ul>
<li><strong>Mercury</strong> is placed at Stockholm City Museum, 2,900 m from the Globe. The small metallic sphere was built by the artist Peter Varhelyi.</li>
<li><strong>Venus</strong> is placed at KTH, 5,500 m from the Globe. The model, made by the United States artist Daniel Oberti, was inaugurated June 8, 2004, during a Venus transit.</li>
<li><strong>Earth</strong> is located at the Swedish Museum of Natural History, 7,600 m from the Globe. An elaborate model of the Moon (18 cm in diameter) is on display in another part of the museum.</li>
<li><strong>Mars</strong> is located at M&#246rby Centrum, a shopping centre in Danderyd, a suburb of Stockholm. It is 11.6 km from the Globe. </li>
</ul>


<h3>Outer planets</h3>
<ol>
<li><strong>Jupiter</strong> is placed at the roundabout near Sky City, in Stockholm Arlanda Airport in Sigtuna Municipality, 40 km from the Globe. It is made as a flower decoration, with different flowers representing different zones of the giant gas planet. </li>
<li><strong>Saturn</strong> is placed outside the old observatory of Anders Celsius, in the so-called Celsius Square, at centre of Uppsala, 73 km from the Globe. </li>
<li><strong>Uranus</strong> was vandalized and the new model is planned for somewhere in G&#246;vle, 143 km from the Globe.</li>
<li><strong>Neptune</strong> is located by the river S&#246;derhamns&#229;n in S&#246;derhamn, a coast town with tradition of fishing and sailing. Placed 229 km from the Globe, the model is made of acrylic and, at night, shines with a blue light.</li>
</ol>

<h3>The Sun</h3>
<p>The Sun is represented by the Ericsson Globe, Stockholm, which is the largest hemispherical building in the world, 110 m in diameter. To respect the scale, the globe represents the Sun including its corona.</p>


